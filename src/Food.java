public class Food {
    public int price;
    public boolean isCheap;

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public boolean getIsCheap() {
        return isCheap;
    }

    public void setIsCheap(boolean cheap) {
        isCheap = cheap;
    }
}
